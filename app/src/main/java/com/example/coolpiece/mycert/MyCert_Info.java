package com.example.coolpiece.mycert;

public class MyCert_Info {
    public String certificate_name;
    public String certificate_serial_num;
    public String birthday;
    public String certificate_date;
    public String certificate_institution;
    public String img_name;

    public String getCertificate_name() {
        return certificate_name;
    }

    public void setCertificate_name(String certificate_name) {
        this.certificate_name = certificate_name;
    }

    public String getCertificate_serial_num() {
        return certificate_serial_num;
    }

    public void setCertificate_serial_num(String certificate_serial_num) {
        this.certificate_serial_num = certificate_serial_num;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getCertificate_date() {
        return certificate_date;
    }

    public void setCertificate_date(String certificate_date) {
        this.certificate_date = certificate_date;
    }

    public String getCertificate_institution() {
        return certificate_institution;
    }

    public void setCertificate_institution(String certificate_institution) {
        this.certificate_institution = certificate_institution;
    }

    public String getImg_name() {
        return img_name;
    }

    public void setImg_name(String img_name) {
        this.img_name = img_name;
    }
}
