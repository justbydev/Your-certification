package com.example.coolpiece.splash.dataclass;

public class Academy {
    String academy_name;
    String academy_address;
    int academy_post;
    String academy_number;
    String academy_cert;
    int connect;
    public Academy() {
    }

    public String getAcademy_name() {
        return academy_name;
    }

    public void setAcademy_name(String academy_name) {
        this.academy_name = academy_name;
    }

    public String getAcademy_address() {
        return academy_address;
    }

    public void setAcademy_address(String academy_address) {
        this.academy_address = academy_address;
    }

    public int getAcademy_post() {
        return academy_post;
    }

    public void setAcademy_post(int academy_post) {
        this.academy_post = academy_post;
    }

    public String getAcademy_number() {
        return academy_number;
    }

    public void setAcademy_number(String academy_number) {
        this.academy_number = academy_number;
    }

    public String getAcademy_cert() {
        return academy_cert;
    }

    public void setAcademy_cert(String academy_cert) {
        this.academy_cert = academy_cert;
    }

    public int getConnect() {
        return connect;
    }

    public void setConnect(int connect) {
        this.connect = connect;
    }
}
